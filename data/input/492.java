public class SystemPropertiesCommandlet implements Commandlet {
    private static final Logger LOG = Logger.getLogger(SystemPropertiesCommandlet.class);
    public void execute(OutputStream output) throws Exception {
        System.getProperties().store(output, "Generated by " + getClass().getName());
    }
    public String getContentType() {
        return Commandlet.ContentType.PLAIN;
    }
    public String getTitle() {
        return "Dump System Properties";
    }
}
